function [Y]=MUX8by1(S0,S1,S2,i0,i1,i2,i3,i4,i5,i6,i7)
OUT1=NOT(S0);
OUT2=NOT(S1);
OUT3=NOT(S2);

OUT4=And4(i0,OUT3,OUT2,OUT1);
OUT5=And4(i1,OUT3,OUT2,S0);
OUT6=And4(i2,OUT3,S1,OUT1);
OUT7=And4(i3,OUT3,S1,S0);
OUT8=And4(i4,S2,OUT2,OUT1);
OUT9=And4(i5,S2,OUT2,S0);
OUT10=And4(i6,S2,S1,OUT1);
OUT11=And4(i7,S2,S1,S0);

Y=Or8(OUT4,OUT5,OUT6,OUT7,OUT8,OUT9,OUT10,OUT11);

end